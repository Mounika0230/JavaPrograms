package com.edu;

public class RelationalOperator {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		if (a > b) {
			System.out.println("10 is greater than 20");
		}
		else {
			System.out.println("20 is greater than 10");
		}
		if (a >= b) {
			System.out.println("10 is greater than or equal to 20");
		}
		else {
			System.out.println("20 is greater than or equal to 10");
		}
		if (a!=b) {
			System.out.println("True");
		}
		else {
			System.out.println("False");
		}
		if (a==b) {
			System.out.println("10 is equal equal to 20");
		}
		else {
			System.out.println("20 is not equal to 10");

		}
	}

}
